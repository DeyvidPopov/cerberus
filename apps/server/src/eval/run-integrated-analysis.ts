// Integrated-study analysis runner (M11 / ADR-0014; PROJECT.md §5, §6).
//
//   npm run eval:integrated --workspace @cerberus/server
//
// Reads a LABELED end-to-end attempts file (one JSON object per line; see
// docs/evaluation/README.md for the schema + collection + consent procedure) and
// writes the policy-level metrics to docs/evaluation/. The input is OUTCOMES +
// labels only (never raw telemetry) and lives in the gitignored data dir; only the
// aggregate metrics output is committed. Optional to run — the tooling stands
// regardless; numbers appear once a study is collected.
import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';

import { analyzeAttempts, parseAttemptsJsonl, type IntegratedMetrics, type Rate } from './integrated-study';

const REPO_ROOT = fileURLToPath(new URL('../../../../', import.meta.url));
const DEFAULT_INPUT = `${REPO_ROOT}docs/evaluation/data/integrated-study.jsonl`;
const OUTPUT_DIR = `${REPO_ROOT}docs/evaluation`;

function pct(x: Rate): string {
  return x === null ? 'n/a' : `${(x * 100).toFixed(2)}%`;
}

function summaryMarkdown(m: IntegratedMetrics): string {
  return `# Integrated composite-policy study

Generated by \`npm run eval:integrated --workspace @cerberus/server\` (ADR-0014).

End-to-end metrics of the LIVE composite policy (combiner → band → enforce; plus
continuous-auth spike → lock), computed from labeled attempts the human ran against
the running system. Outcomes + labels only — no raw telemetry (PROJECT.md §5).

- **Attempts:** ${String(m.counts.total)}
  (login: ${String(m.counts.genuineLogins)} genuine / ${String(m.counts.impostorLogins)} impostor;
  continuous: ${String(m.counts.genuineContinuous)} genuine / ${String(m.counts.impostorContinuous)} impostor)

| Metric | Value |
|--------|------:|
| Composite FAR (impostor logins effectively granted) | ${pct(m.compositeFar)} |
| Composite FRR (genuine logins hard-denied) | ${pct(m.compositeFrr)} |
| Step-up rate (all logins) | ${pct(m.stepUpRate)} |
| False-step-up rate (genuine logins stepped up) | ${pct(m.falseStepUpRate)} |
| Impostor caught rate (stepped-up or denied) | ${pct(m.impostorCaughtRate)} |
| False-lock rate (genuine continuous sessions locked) | ${pct(m.falseLockRate)} |
| True-lock rate (impostor continuous sessions locked) | ${pct(m.trueLockRate)} |

_\`n/a\` means no attempts of that kind were collected. Step-up is friction, not a
rejection (a genuine user completes TOTP); a deny is a hard block._
`;
}

function main(): void {
  const input = process.env.INTEGRATED_STUDY_INPUT ?? DEFAULT_INPUT;
  if (!existsSync(input)) {
    console.error(
      `Labeled-attempts file not found at ${input}\n` +
        `Collect a study into docs/evaluation/data/integrated-study.jsonl (see docs/evaluation/README.md)\n` +
        `or set INTEGRATED_STUDY_INPUT. The tooling is optional to run.`,
    );
    process.exitCode = 1;
    return;
  }

  const attempts = parseAttemptsJsonl(readFileSync(input, 'utf8'));
  const metrics = analyzeAttempts(attempts);

  mkdirSync(OUTPUT_DIR, { recursive: true });
  const jsonPath = `${OUTPUT_DIR}/integrated-study.json`;
  const mdPath = `${OUTPUT_DIR}/integrated-study.md`;
  writeFileSync(jsonPath, `${JSON.stringify(metrics, null, 2)}\n`, 'utf8');
  writeFileSync(mdPath, summaryMarkdown(metrics), 'utf8');

  console.log(`Analyzed ${String(metrics.counts.total)} labeled attempts.`);
  console.log(`  composite FAR=${pct(metrics.compositeFar)}  FRR=${pct(metrics.compositeFrr)}`);
  console.log(`  step-up=${pct(metrics.stepUpRate)}  false-step-up=${pct(metrics.falseStepUpRate)}  false-lock=${pct(metrics.falseLockRate)}`);
  console.log(`\nWrote:\n  ${jsonPath}\n  ${mdPath}`);
}

main();
