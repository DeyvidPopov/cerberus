// Reproducible band-threshold tuning (M11 / ADR-0014; PROJECT.md §6).
//
// Sweeps the production behavioral score (Mahalanobis→χ² CDF) over a VALIDATION
// split of the CMU data that is DISJOINT from the K&M test set, and writes the
// recommended login operating point to docs/evaluation/. The chosen values are set
// (by hand, with rationale) as the named band config in risk/config.ts — this
// script is the auditable derivation, regenerable with:
//
//   npm run eval:tune --workspace @cerberus/server
import { existsSync, mkdirSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';

import { groupCmuBySubject, loadCmuDataset } from '../risk/cmu-loader';
import { DEFAULT_TUNING_CONFIG } from '../risk/config';
import { tuneThresholds, type TuningResult } from '../risk/threshold-tuning';

const REPO_ROOT = fileURLToPath(new URL('../../../../', import.meta.url));
const DEFAULT_DATASET = `${REPO_ROOT}docs/evaluation/data/DSL-StrongPasswordData.csv`;
const OUTPUT_DIR = `${REPO_ROOT}docs/evaluation`;

function pct(x: number): string {
  return `${(x * 100).toFixed(2)}%`;
}

function summaryMarkdown(result: TuningResult): string {
  const sweepRows = result.sweep
    .map((r) => `| ${r.composite.toFixed(2)} | ${r.scoreThreshold.toFixed(2)} | ${pct(r.far)} | ${pct(r.frr)} |`)
    .join('\n');

  return `# Login band-threshold tuning

Generated by \`npm run eval:tune --workspace @cerberus/server\` (ADR-0014).
Reproducible: fixed seed \`${String(result.seed)}\`.

Sweeps the **production behavioral score** (Mahalanobis → χ² CDF, the value
\`scoreSample\` returns at login) on a VALIDATION split that is DISJOINT from the
K&M test set used for the reported keystroke EER (**no tuning-on-test**, PROJECT.md §6):

- baseline fit on reps \`[0, ${String(DEFAULT_TUNING_CONFIG.trainSize)})\`;
- genuine validation on reps \`[${String(DEFAULT_TUNING_CONFIG.trainSize)}, ${String(DEFAULT_TUNING_CONFIG.validationGenuineEnd)})\`;
- impostor validation on other subjects' reps \`[${String(DEFAULT_TUNING_CONFIG.impostorStart)}, ${String(DEFAULT_TUNING_CONFIG.impostorEnd)})\`
  (disjoint from the K&M impostor test, reps \`[0, ${String(DEFAULT_TUNING_CONFIG.impostorStart)})\`);
- the K&M genuine test set, reps \`[${String(DEFAULT_TUNING_CONFIG.validationGenuineEnd)}, end)\`, is **never read** here.

- **Subjects:** ${String(result.subjects)} · **Validation genuine:** ${String(result.validationGenuineCount)} · **Validation impostor:** ${String(result.validationImpostorCount)}
- **Behavioral combiner weight:** ${String(result.behavioralWeight)} (composite when only behavioral fires = weight · score).

The keystroke χ² score is a **soft** login signal: genuine validation scores cluster
near 0 (good fit) while impostors spread high. Its equal-error point sits at a tiny
score threshold (≈ ${result.eer.threshold.toFixed(3)}, EER **${pct(result.eer.rate)}**) — but operating there
would step up **${pct(result.eer.frr)}** of genuine logins, which is poor UX. So step-up is set to
the most sensitive composite threshold that keeps the genuine false-step-up rate within
budget; the residual behavioral FAR is closed by **contextual stacking + the TOTP
step-up** (ADR-0012), not by the behavioral signal alone.

## Step-up sweep (FAR/FRR on the composite, behavioral-only)

| composite step-up | behavioral score θ | FAR (impostor pass) | FRR (genuine step-up) |
|------------------:|-------------------:|--------------------:|----------------------:|
${sweepRows}

## Chosen operating point

- **stepUp = ${result.recommendedBands.stepUp.toFixed(2)}** — the most sensitive composite threshold whose
  genuine false-step-up rate stays ≤ ${pct(result.chosen.maxFrr)} on validation (here FRR ${pct(result.chosen.frr)},
  behavioral-only FAR ${pct(result.chosen.far)}). Lower thresholds cut FAR only marginally while
  multiplying genuine step-ups; the combiner + context carry the rest.
- **deny = ${result.recommendedBands.deny.toFixed(2)}** — kept above the maximum single-signal contribution
  (max weight = 0.5), so a deny requires STACKED signals (ADR-0012). No single
  behavioral or contextual signal can deny alone.

_The live values are the named config in \`apps/server/src/risk/config.ts\`
(DEFAULT_BAND_THRESHOLDS / DEFAULT_COMBINER_WEIGHTS), set with this rationale._
`;
}

function main(): void {
  const datasetPath = process.env.CMU_DATASET_PATH ?? DEFAULT_DATASET;
  if (!existsSync(datasetPath)) {
    console.error(
      `CMU dataset not found at ${datasetPath}\n` +
        `Fetch it into docs/evaluation/data/ (see docs/evaluation/README.md) or set CMU_DATASET_PATH.`,
    );
    process.exitCode = 1;
    return;
  }

  const data = groupCmuBySubject(loadCmuDataset(datasetPath));
  const result = tuneThresholds(data, DEFAULT_TUNING_CONFIG);

  mkdirSync(OUTPUT_DIR, { recursive: true });
  const jsonPath = `${OUTPUT_DIR}/threshold-tuning.json`;
  const mdPath = `${OUTPUT_DIR}/threshold-tuning.md`;
  writeFileSync(jsonPath, `${JSON.stringify(result, null, 2)}\n`, 'utf8');
  writeFileSync(mdPath, summaryMarkdown(result), 'utf8');

  console.log('Validation operating points (behavioral score θ):');
  console.log(`  EER     θ=${result.eer.threshold.toFixed(4)}  FAR=${pct(result.eer.far)}  FRR=${pct(result.eer.frr)}  (EER ${pct(result.eer.rate)})`);
  console.log(`  chosen  θ=${result.chosen.threshold.toFixed(4)}  composite=${result.chosen.composite}  FAR=${pct(result.chosen.far)}  FRR=${pct(result.chosen.frr)}  (FRR budget ${pct(result.chosen.maxFrr)})`);
  console.log(`Recommended bands: stepUp=${result.recommendedBands.stepUp}  deny=${result.recommendedBands.deny}`);
  console.log(`\nWrote:\n  ${jsonPath}\n  ${mdPath}`);
}

main();
