// Reproducible offline evaluation runner (ADR-0010; PROJECT.md §6).
//
// Loads the CMU keystroke-dynamics dataset via the M6 loader (same extractor),
// runs the Killourhy & Maxion (2009) protocol with all three detectors, and
// writes machine-readable results + a human summary to docs/evaluation/. Every
// number in the thesis evaluation chapter is regenerable by running this:
//
//   npm run eval:keystroke --workspace @cerberus/server
//
// The dataset is NOT committed (real human captures, PROJECT.md §5). Fetch it
// first into docs/evaluation/data/ (see docs/evaluation/README.md) or set
// CMU_DATASET_PATH. Deterministic: a fixed seed reproduces identical numbers.
import { existsSync, mkdirSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';

import { DEFAULT_EVALUATION_CONFIG } from '../risk/config';
import { createDetectors } from '../risk/detectors';
import { groupCmuBySubject, loadCmuDataset } from '../risk/cmu-loader';
import { runEvaluation, type EvaluationReport } from '../risk/evaluation';

const REPO_ROOT = fileURLToPath(new URL('../../../../', import.meta.url));
const DEFAULT_DATASET = `${REPO_ROOT}docs/evaluation/data/DSL-StrongPasswordData.csv`;
const OUTPUT_DIR = `${REPO_ROOT}docs/evaluation`;

// Published Killourhy & Maxion (2009) EERs for the two classic detectors, cited
// for comparison only (isolation forest is a later addition, not in their study).
const PUBLISHED = {
  mahalanobis: 0.11,
  'one-class-svm': 0.102,
} as const;

function pct(x: number): string {
  return `${(x * 100).toFixed(2)}%`;
}

function summaryMarkdown(report: EvaluationReport, datasetPath: string): string {
  const rows = report.detectors
    .map((d) => {
      const published = (PUBLISHED as Record<string, number>)[d.name];
      const publishedCol = published === undefined ? '—' : pct(published);
      return `| ${d.name} | ${pct(d.meanEer)} | ${pct(d.stdEer)} | ${pct(d.meanFar)} | ${pct(d.meanFrr)} | ${publishedCol} |`;
    })
    .join('\n');

  return `# Keystroke-dynamics detector comparison

Generated by \`npm run eval:keystroke --workspace @cerberus/server\` (ADR-0010).
Reproducible: fixed seed \`${String(report.seed)}\`; re-running yields identical numbers.

- **Protocol:** Killourhy & Maxion (2009). Per subject: train on the first
  ${String(report.trainSize)} genuine reps; test on the held-out genuine reps + the
  first ${String(report.impostorReps)} reps of every other subject.
- **Subjects:** ${String(report.subjects)} · **Feature dimension:** ${String(report.dimension)}
  (position-indexed hold/DD/UD, the shared M6 extractor).
- **Dataset:** \`${datasetPath.replace(REPO_ROOT, '')}\` (NOT committed — real human
  captures, PROJECT.md §5). At the equal-error operating point FAR = FRR = EER.

| Detector | mean EER | SD EER | mean FAR | mean FRR | K&M 2009 |
|----------|---------:|-------:|---------:|---------:|---------:|
${rows}

_K&M 2009 column: the originally published EER for the two classic detectors
(Mahalanobis ≈ 11.0%, one-class SVM ≈ 10.2%); isolation forest is a later addition
(ADR-0002) with no figure in that study. Small differences from published numbers
are expected (regularization choice, EER interpolation, implementation detail)._
`;
}

function main(): void {
  const datasetPath = process.env.CMU_DATASET_PATH ?? DEFAULT_DATASET;
  if (!existsSync(datasetPath)) {
    console.error(
      `CMU dataset not found at ${datasetPath}\n` +
        `Fetch it into docs/evaluation/data/ (see docs/evaluation/README.md) or set CMU_DATASET_PATH.`,
    );
    process.exitCode = 1;
    return;
  }

  console.log(`Loading dataset: ${datasetPath}`);
  const samples = loadCmuDataset(datasetPath);
  const dataBySubject = groupCmuBySubject(samples);
  console.log(`Subjects: ${String(dataBySubject.size)} · Samples: ${String(samples.length)}`);

  const config = DEFAULT_EVALUATION_CONFIG;
  console.log('Running Killourhy & Maxion protocol (Mahalanobis, one-class SVM, isolation forest)…');
  const report = runEvaluation(dataBySubject, createDetectors(config), config);

  mkdirSync(OUTPUT_DIR, { recursive: true });
  const jsonPath = `${OUTPUT_DIR}/keystroke-detector-comparison.json`;
  const mdPath = `${OUTPUT_DIR}/keystroke-detector-comparison.md`;
  writeFileSync(jsonPath, `${JSON.stringify(report, null, 2)}\n`, 'utf8');
  writeFileSync(mdPath, summaryMarkdown(report, datasetPath), 'utf8');

  console.log('\nResults (mean EER ± SD across subjects):');
  for (const d of report.detectors) {
    console.log(`  ${d.name.padEnd(18)} EER ${pct(d.meanEer)} ± ${pct(d.stdEer)}`);
  }
  console.log(`\nWrote:\n  ${jsonPath}\n  ${mdPath}`);
}

main();
