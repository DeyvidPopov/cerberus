// Reproducible offline mouse-dynamics evaluation (M11 / ADR-0014; PROJECT.md §6).
//
// MIRRORS the keystroke runner (run-keystroke-eval.ts): loads the Balabit Mouse
// Dynamics Challenge via the M11 loader (which runs the SAME deployed M10 mouse
// extractor), runs the per-user protocol with the SAME three detectors + the SAME
// runEvaluation, and writes machine-readable + human results to docs/evaluation/.
//
//   npm run eval:mouse --workspace @cerberus/server
//
// The dataset is NOT committed (real human captures, PROJECT.md §5). Fetch it into
// docs/evaluation/data/balabit/ (see docs/evaluation/README.md) or set
// BALABIT_DATASET_DIR. Deterministic: a fixed seed reproduces identical numbers.
import { existsSync, mkdirSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';

import { loadBalabitByUser } from '../risk/balabit-loader';
import { DEFAULT_MOUSE_EVALUATION_CONFIG, DEFAULT_MOUSE_WINDOW_CONFIG } from '../risk/config';
import { createDetectors } from '../risk/detectors';
import { runEvaluation, type EvaluationReport } from '../risk/evaluation';

const REPO_ROOT = fileURLToPath(new URL('../../../../', import.meta.url));
const DEFAULT_DATASET_DIR = `${REPO_ROOT}docs/evaluation/data/balabit`;
const OUTPUT_DIR = `${REPO_ROOT}docs/evaluation`;

function pct(x: number): string {
  return `${(x * 100).toFixed(2)}%`;
}

function summaryMarkdown(report: EvaluationReport, datasetDir: string): string {
  const rows = report.detectors
    .map(
      (d) =>
        `| ${d.name} | ${pct(d.meanEer)} | ${pct(d.stdEer)} | ${pct(d.meanFar)} | ${pct(d.meanFrr)} |`,
    )
    .join('\n');

  return `# Mouse-dynamics detector comparison

Generated by \`npm run eval:mouse --workspace @cerberus/server\` (ADR-0014).
Reproducible: fixed seed \`${String(report.seed)}\`; re-running yields identical numbers.

- **Protocol:** per-user, mirroring the keystroke setup (Killourhy & Maxion style).
  Per user: train on the first ${String(report.trainSize)} of the user's genuine
  feature windows; test on the held-out genuine windows + the first
  ${String(report.impostorReps)} windows of every OTHER user (impostors).
- **Users:** ${String(report.subjects)} · **Feature dimension:** ${String(report.dimension)}
  (velocity / acceleration / curvature / click + pause statistics — the deployed
  M10 \`extractMouseWindowFeatures\`, over a ${String(DEFAULT_MOUSE_WINDOW_CONFIG.windowSize)}-sample
  window; non-overlapping for the benchmark).
- **Dataset:** Balabit Mouse Dynamics Challenge (\`${datasetDir.replace(REPO_ROOT, '')}\`),
  NOT committed — real human captures, PROJECT.md §5. At the equal-error operating
  point FAR = FRR = EER.

| Detector | mean EER | SD EER | mean FAR | mean FRR |
|----------|---------:|-------:|---------:|---------:|
${rows}

_Mouse dynamics is a noisier modality than keystroke: per-user EER is higher and the
SD across users is larger (movement style varies more by task/hardware than fixed-text
typing). The SD column is reported precisely because the per-user spread matters — a
mean alone would understate the variability. These numbers describe the per-window
discriminability of the deployed extractor; the live system smooths windows with an
EWMA before acting (ADR-0013), trading latency for far fewer false locks._
`;
}

function main(): void {
  const datasetDir = process.env.BALABIT_DATASET_DIR ?? DEFAULT_DATASET_DIR;
  if (!existsSync(`${datasetDir}/training_files`)) {
    console.error(
      `Balabit dataset not found at ${datasetDir}/training_files\n` +
        `Fetch it into docs/evaluation/data/balabit/ (see docs/evaluation/README.md) or set BALABIT_DATASET_DIR.`,
    );
    process.exitCode = 1;
    return;
  }

  console.log(`Loading Balabit dataset: ${datasetDir}`);
  const dataByUser = loadBalabitByUser(datasetDir, DEFAULT_MOUSE_WINDOW_CONFIG);
  const totalWindows = [...dataByUser.values()].reduce((acc, w) => acc + w.length, 0);
  console.log(`Users: ${String(dataByUser.size)} · Windows: ${String(totalWindows)}`);

  const config = DEFAULT_MOUSE_EVALUATION_CONFIG;
  console.log('Running per-user protocol (Mahalanobis, one-class SVM, isolation forest)…');
  const report = runEvaluation(dataByUser, createDetectors(config), config);

  mkdirSync(OUTPUT_DIR, { recursive: true });
  const jsonPath = `${OUTPUT_DIR}/mouse-detector-comparison.json`;
  const mdPath = `${OUTPUT_DIR}/mouse-detector-comparison.md`;
  writeFileSync(jsonPath, `${JSON.stringify(report, null, 2)}\n`, 'utf8');
  writeFileSync(mdPath, summaryMarkdown(report, datasetDir), 'utf8');

  console.log('\nResults (mean EER ± SD across users):');
  for (const d of report.detectors) {
    console.log(`  ${d.name.padEnd(18)} EER ${pct(d.meanEer)} ± ${pct(d.stdEer)}`);
  }
  console.log(`\nWrote:\n  ${jsonPath}\n  ${mdPath}`);
}

main();
